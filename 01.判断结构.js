// if 语句
var a = 10;
var b = 2;
if (a > 2) {
  console.log(a / b);
}

// if-else 语句
var c = 10;
var d = 5;
if (c > d) {
  console.log(c / d);
} else {
  console.log(d)
}

// if-else if 语句
var e = 10;
var f = 5;
var g = null
if (e > 5) {
  g = e / f
  console.log(g)
} else if (e === f) {
  g = e + f
  console.log(g)
} else {
  g = e * f
  console.log(g)
}
